+(NSString*)applicationVersionString {
    NSDictionary *info = [[NSBundle mainBundle] infoDictionary];
    return [info objectForKey: @"CFBundleShortVersionString"];
}

+(NSString*) applicationShortVersionString {
    NSMutableArray * versionNumbers = [[[self applicationVersionString] componentsSeparatedByString:@"."] mutableCopy];
    
    if (versionNumbers.count > 2) {
        [versionNumbers removeObjectsInRange:NSMakeRange(2, versionNumbers.count - 2)];
    }
    
    return [versionNumbers componentsJoinedByString:@"."];
}

+(NSString *)fullDeviceInfo {
    NSMutableString *fullInfo = [NSMutableString string];
    [fullInfo appendFormat:@"Device: %@\n", [self platformString]];
    [fullInfo appendFormat:@"App Version: %@\n", [self applicationVersionString]];
    return [fullInfo copy];
}

